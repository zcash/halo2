// ANCHOR: config
/// The config that will implement our instructions! Configs do not store any persistent
/// state themselves, and usually only contain type markers if necessary.
struct FieldConfig<F: FieldExt> {
    _marker: PhantomData<F>,
}

impl<F: FieldExt> Config for FieldConfig<F> {
    type Configured = FieldConfigured;
    type Loaded = ();
    type Field = F;
    fn load(layouter: &mut impl Layouter<Self>) -> Result<Self::Loaded, Error> {
        Ok(())
    }
}
// ANCHOR_END: config

// ANCHOR: configured
/// Configured state is stored in a separate config struct. This is generated by the config
/// during configuration, and then handed to the `Layouter`, which makes it available
/// to the config when it needs to implement its instructions.
#[derive(Clone, Debug)]
struct FieldConfigured {
    s_pub: Selector,
    perm: Permutation,
    advice: [Column<Advice>; 2],
    add_configured: AddConfigured,
    mul_configured: MulConfigured,
}

impl<F: FieldExt> FieldConfig<F> {
    fn configure(
        meta: &mut ConstraintSystem<F>,
        advice: [Column<Advice>; 2],
        instance: Column<Instance>,
    ) -> FieldConfigured {
        let perm = Permutation::new(
            meta,
            &advice
                .iter()
                .map(|column| (*column).into())
                .collect::<Vec<_>>(),
        );
        let s_pub = meta.selector();
        let s_add = meta.selector();
        let s_mul = meta.selector();

        let add_configured = AddConfig::configure(meta, perm.clone(), advice, s_add);
        let mul_configured = MulConfig::configure(meta, perm.clone(), advice, s_mul);

        FieldConfigured {
            s_pub,
            perm,
            advice,
            add_configured,
            mul_configured,
        }
    }
}
// ANCHOR_END: configured

impl<F: FieldExt> NumericInstructions for FieldConfig<F> {
    type Num = Number<F>;

    fn load_private(
        layouter: &mut impl Layouter<Self>,
        value: Option<F>,
    ) -> Result<Self::Num, Error> {
        let configured = layouter.configured().clone();
        let mut num = None;
        layouter.assign_region(
            || "load private",
            |mut region| {
                let cell = region.assign_advice(
                    || "private input",
                    configured.advice[0],
                    0,
                    || value.ok_or(Error::SynthesisError),
                )?;
                num = Some(Number { cell, value });
                Ok(())
            },
        )?;
        Ok(num.unwrap())
    }

    fn add<CS: Assignment<Self::Field>>(
        layouter: &mut SingleConfigLayouter<'_, Self, CS>,
        a: Self::Num,
        b: Self::Num,
    ) -> Result<Self::Num, Error> {
        let add_configured = layouter.configured().add_configured.clone();
        let mut add_layouter =
            SingleConfigLayouter::<'_, Self, CS>::sublayouter(layouter.cs, add_configured)?;
        AddConfig::<F>::add(&mut add_layouter, a, b)
    }

    fn mul<CS: Assignment<Self::Field>>(
        layouter: &mut SingleConfigLayouter<'_, Self, CS>,
        a: Self::Num,
        b: Self::Num,
    ) -> Result<Self::Num, Error> {
        let mul_configured = layouter.configured().mul_configured.clone();
        let mut mul_layouter =
            SingleConfigLayouter::<'_, Self, CS>::sublayouter(layouter.cs, mul_configured)?;
        MulConfig::<F>::mul(&mut mul_layouter, a, b)
    }

    fn expose_public(layouter: &mut impl Layouter<Self>, num: Self::Num) -> Result<(), Error> {
        let configured = layouter.configured().clone();
        layouter.assign_region(
            || "expose public",
            |mut region| {
                // Enable the public-input gate.
                configured.s_pub.enable(&mut region, 0)?;

                // Load the output into the correct advice column.
                let out = region.assign_advice(
                    || "public advice",
                    configured.advice[1],
                    0,
                    || num.value.ok_or(Error::SynthesisError),
                )?;
                region.constrain_equal(&configured.perm, num.cell, out)?;

                // We don't assign to the instance column inside the circuit;
                // the mapping of public inputs to cells is provided to the prover.
                Ok(())
            },
        )
    }
}
