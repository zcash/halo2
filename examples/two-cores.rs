// ANCHOR: circuit
/// The full circuit implementation.
///
/// In this struct we store the private input variables. We use `Option<F>` because
/// they won't have any value during key generation. During proving, if any of these
/// were `None` we would get an error.
struct MyCircuit<F: FieldExt> {
    a: Option<F>,
    b: Option<F>,
}

impl<F: FieldExt> Circuit<F> for MyCircuit<F> {
    // Since we are using a single chip for everything, we can just reuse its configured.
    type Configured = FieldConfigured;

    fn configure(meta: &mut ConstraintSystem<F>) -> Self::Configured {
        // We create the two advice columns that FieldChip uses for I/O.
        let advice = [meta.advice_column(), meta.advice_column()];

        // We also need an instance column to store public inputs.
        let instance = meta.instance_column();

        FieldConfig::configure(meta, advice, instance)
    }

    fn synthesize(
        &self,
        cs: &mut impl Assignment<F>,
        configured: Self::Configured,
    ) -> Result<(), Error> {
        let mut layouter = SingleConfigLayouter::new(cs, configured)?;

        // Load our private values into the circuit.
        let a = FieldConfig::<F>::load_private(&mut layouter, self.a)?;
        let b = FieldConfig::<F>::load_private(&mut layouter, self.b)?;

        // ab = a*b
        let ab = FieldConfig::<F>::mul(&mut layouter, a, b)?;
        // c = 2*a*b
        let c = FieldConfig::<F>::add(&mut layouter, ab.clone(), ab)?;

        // Expose the result as a public input to the circuit.
        FieldConfig::expose_public(&mut layouter, c)
    }
}
// ANCHOR_END: circuit

fn main() {
    use halo2::{dev::MockProver, pasta::Fp};

    // ANCHOR: test-circuit
    // The number of rows in our circuit cannot exceed 2^k. Since our example
    // circuit is very small, we can pick a very small value here.
    let k = 6;

    // Prepare the private and public inputs to the circuit!
    let a = Fp::from(2);
    let b = Fp::from(3);
    let c = a * b + a * b;

    // Instantiate the circuit with the private inputs.
    let circuit = MyCircuit {
        a: Some(a),
        b: Some(b),
    };

    // Arrange the public input. We expose the multiplication result in row 6
    // of the instance column, so we position it there in our public inputs.
    let mut public_inputs = vec![Fp::zero(); 1 << k];
    public_inputs[6] = c;

    // Given the correct public input, our circuit will verify.
    let prover = MockProver::run(k, &circuit, vec![public_inputs.clone()]).unwrap();
    assert_eq!(prover.verify(), Ok(()));

    // If we try some other public input, the proof will fail!
    public_inputs[6] += Fp::one();
    let prover = MockProver::run(k, &circuit, vec![public_inputs]).unwrap();
    assert_eq!(
        prover.verify(),
        Err(VerifyFailure::Gate {
            gate_index: 1,
            gate_name: "public input",
            row: 6,
        })
    );
    // ANCHOR_END: test-circuit
}
