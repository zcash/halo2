extern crate halo2;

use std::marker::PhantomData;

use halo2::{
    arithmetic::FieldExt,
    circuit::{layouter::SingleConfigLayouter, Cell, Config, Layouter},
    dev::VerifyFailure,
    plonk::{
        Advice, Assignment, Circuit, Column, ConstraintSystem, Error, Instance, Permutation,
        Selector,
    },
    poly::Rotation,
};

#[derive(Clone)]
struct Number<F: FieldExt> {
    cell: Cell,
    value: Option<F>,
}

trait NumericInstructions: Config {
    /// Variable representing a number.
    type Num;

    /// Loads a number into the circuit as a private input.
    fn load_private(
        layouter: &mut impl Layouter<Self>,
        a: Option<Self::Field>,
    ) -> Result<Self::Num, Error>;

    /// Returns `c = a + b`.
    fn add<CS: Assignment<Self::Field>>(
        layouter: &mut SingleConfigLayouter<'_, Self, CS>,
        a: Self::Num,
        b: Self::Num,
    ) -> Result<Self::Num, Error>;

    /// Returns `c = a * b`.
    fn mul<CS: Assignment<Self::Field>>(
        layouter: &mut SingleConfigLayouter<'_, Self, CS>,
        a: Self::Num,
        b: Self::Num,
    ) -> Result<Self::Num, Error>;

    /// Exposes a number as a public input to the circuit.
    fn expose_public(layouter: &mut impl Layouter<Self>, num: Self::Num) -> Result<(), Error>;
}
// ANCHOR_END: instructions

